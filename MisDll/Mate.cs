namespace Clases
{
    public class Mate
    {
        public static double Suma(double pA, double pB)
        {
            return pA + pB;
        }
        public static double Recta(double pA, double pB)
        {
            return pA - pB;
        }
        public static double Muti(double pA, double pB)
        {
            return pA * pB;
        }
        public static double Div(double pA, double pB)
        {
            return pA / pB;
        }
    }
}
